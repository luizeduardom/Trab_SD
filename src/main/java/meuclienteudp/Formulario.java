    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String msg = jTextField3.getText();
        
        if(msg.isEmpty()){
            JOptionPane.showConfirmDialog(null, "Favor informar MENSAGEM a ser enviada.");
            jTextField3.requestFocus();
        }else{
            msg = "(" + jTextField2.getText() + "): " + msg;
            
            MeuClienteUDP cliente = new MeuClienteUDP();
            String resposta = cliente.enviarMensagem(msg);
            
            jTextArea1.setText(resposta);
            jTextField3.setText("");
            jTextField3.requestFocus();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

